package org.example;

public class Result {
    private int indedx;
    private double result;
    public Result(int indedx, double result) {
        this.indedx = indedx;
        this.result = result;
    }

    public int getIndedx() {
        return indedx;
    }

    public double getResult() {
        return result;
    }
}
